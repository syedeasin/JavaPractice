package JavaOOPLearning;

public class MultiAttribute {

    String firstName = "Syed Mohammad";
    String lastName = "Easin";
    int age = 26;
    public static void main(String[] args) {
        MultiAttribute multiAttribute = new MultiAttribute();
        System.out.println("Full Name: " + multiAttribute.firstName + " " + multiAttribute.lastName + "\nAge: " + multiAttribute.age);

    }
}
