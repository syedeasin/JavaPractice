package JavaOOPLearning;

public class EncapsulationExample1 {
    private int age;
    private String name = "Yeamin";
    public int getAge()
    {
        return age;
    }
    public void setAge(int newAge)
    {
        this.age = newAge;
    }

    public String getName() {
        return name;
    }
}
